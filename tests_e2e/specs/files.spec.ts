test.describe("Files Page", () => {
  test.beforeEach(async ({ page }) => {
    await page.goto("/data/files");
    await page.waitForLoadState("networkidle");
  });

  test("should load files page without errors", async ({ page }) => {
    // Page should load without crashing
    await expect(page).toHaveURL(/\/data\/files/);

    // No error banners
    const errorAlert = page.getByRole("alert");
    const errorCount = await errorAlert.count();
    // Soft check — some alerts may be informational
  });

  test("should display file upload interface or file list", async ({
    page,
  }) => {
    // Look for upload controls
    const uploadInput = page.locator('input[type="file"]');
    const uploadBtn = page
      .getByRole("button", {
        name: /загрузить|upload|добавить|add|выбрать файл|choose file/i,
      })
      .first();
    const dropZone = page.locator(
      '[class*="upload"], [class*="drop"], [class*="file"]'
    );

    const hasUploadInput = await uploadInput
      .isVisible({ timeout: 2000 })
      .catch(() => false);
    const hasUploadBtn = await uploadBtn
      .isVisible({ timeout: 2000 })
      .catch(() => false);
    const hasDropZone = (await dropZone.count()) > 0;

    // Should have some form of file interface
    expect(hasUploadInput || hasUploadBtn || hasDropZone).toBeTruthy();
  });

  test("should show uploaded files list if files exist", async ({ page }) => {
    // Check for file list (table or cards)
    const table = page.getByRole("table");
    const fileItems = page.locator(
      '[class*="file"], [class*="item"]'
    );

    const hasTable = await table
      .isVisible({ timeout: 2000 })
      .catch(() => false);
    const hasItems = (await fileItems.count()) > 0;

    // Either shows files or empty state — both are valid
  });

  test("should navigate to files page from sidebar", async ({ page }) => {
    await page.goto("/dashboard");
    await page.getByRole("link", { name: "Файлы" }).click();
    await expect(page).toHaveURL(/\/data\/files/);
  });
});

